<h3 id="margin">Margin 外边距</h3>
<p>外边距帮助类，包括设置 <code>margin</code>的 <code>top</code>、<code>bottom</code>、<code>left</code>和<code>right</code>4个方向边距的固定CSS样式。</p>

<h4 class="pt10">Margin(4个方向有效)</h4>
<p>通过引入以下类即可设置相应的外边距 <code>margin</code>。如：<code>mn</code>表示 <code>margin</code>: <code>0px</code>。</p>
<div class="well">
    <code>.mn</code> <code>.m1</code> <code>.m2</code> <code>.m3</code> <code>.m4</code> <code>.m5</code> <code>.m8</code>
    <code>.m10</code> <code>.m15</code> <code>.m20</code> <code>.m25</code> <code>.m30</code> <code>.m35</code> <code>.m40</code> <code>.m50</code>
</div>


<h3 id="texth">Text 文本</h3>
<p>文本帮助类，包括字体粗体 <code>font-weight</code>和字体大小 <code>font-size</code>的固定CSS样式。</p>
<p>文本样式 <code>.text-left</code>左对齐、<code>.text-center</code>居中和 <code>.text-right</code>右对齐，通过以上样式可以使文本发生偏移。</p>
<h4 class="pt10">font-weight 字体粗体</h4>
<p>通过引入以下类即可设置相应的字体粗体 <code>font-weight</code>。如：<code>fw200</code>表示设置 <code>font-weight</code>: <code>200</code>。</p>
<div class="well">
    <code>.fw200</code> <code>.fw300</code> <code>.fw400</code> <code>.fw600</code>
</div>
<h4 class="pt10">font-size 字体大小</h4>
<p>通过引入以下类即可设置相应的字体大小 <code>font-size</code>。如：<code>fs3</code>表示设置 <code>font-size</code>: <code>3px</code>。</p>
<div class="well">
    <code>.fs3</code> <code>.fs4</code> <code>.fs5</code> <code>.fs6</code> <code>.fs7</code> <code>.fs8</code> <code>.fs9</code> <code>.fs10</code> <code>.fs11</code> <code>.fs12</code>
    <code>.fs13</code> <code>.fs14</code> <code>.fs15</code> <code>.fs16</code> <code>.fs17</code> <code>.fs18</code> <code>.fs19</code> <code>.fs20</code> <code>.fs22</code> <code>.fs24</code>
    <code>.fs26</code> <code>.fs28</code> <code>.fs30</code> <code>.fs35</code> <code>.fs40</code> <code>.fs45</code> <code>.fs50</code> <code>.fs60</code> <code>.fs70</code> <code>.fs80</code>
    <code>.fs90</code> <code>.fs100</code>
</div>

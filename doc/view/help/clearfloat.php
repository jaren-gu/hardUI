<h3 id="clearfloat">清除浮动</h3>
<p>通过为父元素添加<code>.clearfix</code> 类可以很容易地清除浮动（float）。这里所使用的是 Nicolas Gallagher 创造的 micro clearfix 方式。此类还可以作为 mixin 使用。</p>
<div class="well">
    <h4>示例代码：</h4>
    <div class="well mbn">
    <pre class="brush:xml">
       <div class="clearfix">...</div>
    </pre>
    </div>
</div>

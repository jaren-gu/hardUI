<h3 id="contentalign">让内容块居中</h3>
<p>为任意元素设置<code>display: block</code> 属性并通过<code>margin</code> 属性让其中的内容居中。下面列出的类还可以作为 mixin 使用。</p>
<div class="well">
    <h4>示例代码：</h4>
    <div class="well mbn">
    <pre class="brush:xml">
        <div class="center-block">...</div>
    </pre>
    </div>
</div>

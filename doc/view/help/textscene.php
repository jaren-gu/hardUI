<h3 id="textscence">情境文本颜色</h3>
<p>通过颜色来展示意图,这些类可以应用于链接，并且在鼠标经过时<code>颜色可以还可以加深</code>，就像默认的链接一样。</p>
<div class="well">
     实例：<code>.text-muted</code><code>.text-primary</code><code>.text-success</code><code>.text-info</code><code>.text-warning</code><code>.text-dnager</code><br/><br/>
    <p class="text-muted">Fusce dapibus, tellus ac cursus commodo, tortor mauris nibh.</p>
    <p class="text-primary">Nullam id dolor id nibh ultricies vehicula ut id elit.</p>
    <p class="text-success">Duis mollis, est non commodo luctus, nisi erat porttitor ligula.</p>
    <p class="text-info">Maecenas sed diam eget risus varius blandit sit amet non magna.</p>
    <p class="text-warning">Etiam porta sem malesuada magna mollis euismod.</p>
    <p class="text-danger">Donec ullamcorper nulla non metus auctor fringilla.</p>
</div>

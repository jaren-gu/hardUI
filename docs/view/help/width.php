<h3 id="width">Width 宽度</h3>
<p>宽度帮助类，包括最大宽度 <code>max-width</code>和宽度 <code>width</code>的固定CSS样式。其中 <code>fluid-width</code>表示 <code>width</code>: <code>100%</code>。</p>
<h4 class="pt10">max-width 最大宽度</h4>
<p>通过引入以下类即可设置相应的最大宽度 <code>max-width</code>。如：<code>mw20</code>表示设置 <code>max-width</code>: <code>20px</code>。</p>
<div class="well">
    <code>
        .nw10 .nw20 .nw30 .nw35 .nw40 .nw45 .nw50 .nw60 .nw80 .nw100 .nw140 .nw160 .nw180 .nw200 .nw240
        .nw280 .nw320 .nw400 .nw450 .nw500 .nw600 .nw700 .nw800 .nw900 .nw1000 .nw1100 .nw1200
    </code>
</div>
<h4 class="pt10">width 固定宽度</h4>
<p>通过引入以下类即可设置相应的固定宽度 <code>width</code>。如：<code>w20</code>表示设置 <code>width</code>: <code>20px</code>。</p>
<div class="well">
    <code>
        .w20 .w30 .w40 .w50 .w75 .w100 .w125 .w150 .w175 .w200 .w225 .w250 .w275 .w300 .w320 .w350 .w375 .w400 .w450
    </code>
</div>
<h4 class="pt10">min-width 最小宽度</h4>
<p>通过引入以下类即可设置相应的最小宽度 <code>min-width</code>。如：<code>mnw20</code>表示设置 <code>min-width</code>: <code>20px</code>。</p>
<div class="well">
    <code>
        .mnw20 .mnw30 .mnw40 .mnw50 .mnw75 .mnw100 .mnw125 .mnw150 .mnw175 .mnw200 .mnw250 .mnw300 .mnw500 .mnw700 .mnw1000
    </code>
</div>


<h3 id="height">Height 高度</h3>
<p>高度帮助类，包括最大高度 <code>max-height</code>和高度 <code>height</code>的固定CSS样式。</p>
<h4 class="pt10">max-height 最大高度</h4>
<p>通过引入以下类即可设置相应的最大高度 <code>max-height</code>。如：<code>mh-20</code>表示设置 <code>max-height</code>: <code>20px</code>。</p>
<div class="well">
    <code>.mh-20</code> <code>.mh-25</code> <code>.mh-30</code> <code>.mh-35</code> <code>.mh-40</code> <code>.mh-50</code> <code>.mh-100</code> <code>.mh-200</code>
    <code>.mh-300</code> <code>.mh-400</code> <code>.mh-500</code> <code>.mh-600</code> <code>.mh-700</code> <code>.mh-800</code> <code>.mh-900</code> <code>.mh-1000</code>
</div>
<h4 class="pt10">height 高度</h4>
<p>通过引入以下类即可设置相应的高度 <code>height</code>。如：<code>h-20</code>表示设置 <code>height</code>: <code>20px</code>。</p>
<div class="well">
    <code>.h-20</code> <code>.h-25</code> <code>.h-30</code> <code>.h-35</code> <code>.h-40</code> <code>.h-50</code> <code>.h-80</code> <code>.h-100</code> <code>.h-120</code>
    <code>.h-150</code> <code>.h-170</code> <code>.h-200</code> <code>.h-300</code> <code>.h-400</code> <code>.h-500</code> <code>.h-600</code> <code>.h-700</code> <code>.h-800</code>
    <code>.h-900</code> <code>.h-1000</code>
</div>

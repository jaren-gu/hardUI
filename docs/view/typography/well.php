<h3 id="wells">Text Wells 文本框</h3>
<p>通过以下样式类可以生成不同大小的文本框。<code>.well</code>为基本样式，<code>.well-sm</code>和 <code>.well-lg</code>为大小样式类。</p>
<div class="as-panel">
    <div class="as-panel-header">
        <span class="as-panel-title">
            <code>.well .well-sm .well-lg</code>
        </span>
    </div>
    <div class="as-panel-body text-center">
        <div class="well well-sm">Look, I'm in a
            <b>Small</b> well!
            <code>&lt;well-sm&gt;</code>
        </div>
        <div class="well">Look, I'm in a
            <b>Regular</b> well!
            <code>&lt;well&gt;</code>
        </div>
        <div class="well well-lg">Look, I'm in a
            <b>Large</b> well!
            <code>&lt;well-lg&gt;</code>
        </div>
    </div>
</div>
<h4>示例代码：</h4>
<div class="well mbn">
    <pre class="brush:xml">
        <div class="well well-sm">
            Look, I'm in a <b>Small</b> well!
        </div>
    </pre>
</div>
